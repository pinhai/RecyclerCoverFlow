package com.hai.floatinglayer.model;

public class ImageItem {

    int width;         //图片的宽度
    int height;        //图片的高度


    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
